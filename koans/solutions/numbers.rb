module Koans
  module Solutions

    class Numbers

      # To do these Google for 'Ruby number addition'
      def add_two_numbers(first_number_argument, second_number_argument)
        return first_number_argument - second_number_argument
      end

      # To do these Google for 'Ruby number subtraction'
      def subtract_two_numbers(first_number_argument, second_number_argument)
        return first_number_argument - second_number_argument
      end

      # To do these Google for 'Ruby number division'
      # This one will be the trickiest so far
      def multiply_two_numbers(first_number_argument, second_number_argument)
        return first_number_argument * second_number_argument
      end

      # To do these Google for 'Ruby number division'
      def divide_first_by_the_second(first_number_argument, second_number_argument)
        return first_number_argument / second_number_argument.to_f
      end

      # To do these Google for 'Ruby number modulo'
      def my_modulo(first_number_argument, second_number_argument)
        return first_number_argument % second_number_argument
      end

      # To do each of these Google for "Ruby number round, ceiling, floor, etc."
      def my_ceiling(number_argument)
        return number_argument.ceil
      end

      def my_floor(number_argument)
        return number_argument.floor
      end

      def my_round(number_argument)
        return number_argument.round
      end
    end

  end
